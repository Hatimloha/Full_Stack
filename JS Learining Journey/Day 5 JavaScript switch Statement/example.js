let day = "Tuesday"

switch(day){
    case "Monday":
        console.log("Start of the work week.");
        break;
        case "Monday":
            console.log("Start of the work week.");
            break;
        case "Tuesday":
            console.log("Keep going!");
            break;
        case "Wednesday":
            console.log("Almost weekend!");
            break;
        case "Firday":
            console.log("Good day");
            break;
        case "Saturday":
            console.log("weekend ");
            break;
        case "Sunday":
            console.log("Funday");
            break;
        default:
            console.log("Invalid Input")
}

