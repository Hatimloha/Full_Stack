//  Practice Exercise 6: Static Methods
// Create a class MathUtil that has a static method square(n) which returns the square of a number.

class MathUtil{
    static square(n){
        return n * n;
    }
}

// Example usage:
console.log(MathUtil.square(5)); // Output: 25
console.log(MathUtil.square(10)); // Output: 100    