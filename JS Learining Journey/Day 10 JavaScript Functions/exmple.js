// Example 1: Function Without Parameters:
function sayHi(){
    console.log("Hi There!");
}
sayHi(); // Hi There!


// Example 2: Function That Adds Numbers:
function add(a, b){
    return a  + b;
}
console.log(add(5, 10)); // 15


// Example 3:  Exercise with Code:
function isEven(num){
    if(num % 2 === 0){
        return true
    }
    else{
        return false
    }
}

console.log(isEven(4)); // true
console.log(isEven(9)); // false


